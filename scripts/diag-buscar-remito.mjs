import fs from 'fs';import path from 'path';import {fileURLToPath} from 'url';
const __dirname=path.dirname(fileURLToPath(import.meta.url));
const env=fs.readFileSync(path.join(__dirname,'..','.env'),'utf8');
const g=k=>((env.match(new RegExp(`^${k}=(.*)$`,'m'))||[])[1]||'').trim().replace(/^"|"$/g,'');
const U=g('NEXT_PUBLIC_SUPABASE_URL'),K=g('SUPABASE_SERVICE_ROLE_KEY');
const H={apikey:K,Authorization:`Bearer ${K}`};
const RN='R-2026-00299';
console.log('=== VENTAS con remito',RN,'===');
const v=await(await fetch(`${U}/rest/v1/ventas?remito_number=eq.${RN}&select=id,sale_number,client_name,seller_name,total,payment_type,remito_number,created_at,status`,{headers:H})).json();
console.log(JSON.stringify(v,null,2));
console.log('=== PEDIDOS con remito',RN,'===');
const p=await(await fetch(`${U}/rest/v1/pedidos?remito_number=eq.${RN}&select=id,client_name,status,remito_number,total,created_at,updated_at`,{headers:H})).json();
console.log(JSON.stringify(p,null,2));
console.log('=== buscar por cliente Pamela/Zaballo en ventas ===');
